package control;

public class Calculadora {

    /**
     * Metodo que soma dois números.
     * @param a xxxxx
     * @param b yyyyy
     * @return um valor double
     */

    public double somar(final double a, final double b) {
        return a + b;
    }

    /**
     * Metodo que subtrai dois números.
     * @param a xxxxx
     * @param b yyyyy
     * @return um valor double
     */
    public double subtrair(final double a, final double b) {
        return a - b;
    }

    /**
     * Metodo que divide dois números.
     * @param a xxxxx
     * @param b yyyyy
     * @return um valor double
     */
    public double dividir(final double a, final double b) {
        return a / b;
    }

    /**
     * Metodo que multiplica dois números.
     * @param a xxxxx
     * @param b yyyyy
     * @return um valor double
     */
    public double multiplicar(final double a, final double b) {
        return a * b;
    }
}
